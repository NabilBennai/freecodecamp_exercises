function checkCashRegister(price, cash, cid) {
  // Calculate the money the cashier has to give back to the customer
  let changeToGive = cash - price;

  // Calculate the value of the money inside the cashdrawer
  let availableMoney =
    Math.round(
      100 * cid.reduce((accumulator, [_, money]) => accumulator + money, 0)
    ) / 100;

  let valuesOfDenominations = [0.01, 0.05, 0.1, 0.25, 1, 5, 10, 20, 100];

  // Create a lookup table which contains the amount of every denomination (for example 2 pennies, 4 $20 bills...)
  let lookup = [];
  for (let index = 0; index < valuesOfDenominations.length; index++) {
    lookup.push([
      valuesOfDenominations[index],
      Math.round((100 * cid[index][1]) / valuesOfDenominations[index]) / 100,
    ]);
  }
  // Go through the lookup table in decreasing order of denomination value and check every time if there is enough change. If not, return the ERROR object

  // let change = [];
  let tmpChangeToGive = changeToGive;
  for (let index = lookup.length - 1; index >= 0; index--) {
    let val = lookup[index][0];
    let nb = lookup[index][1];
    let nbRequired = tmpChangeToGive / val;
    if (tmpChangeToGive <= val * nb && Number.isInteger(nbRequired)) {
      console.log(`Give ${nbRequired} times ${val}`);
    } else {
      console.log(`Does not work with value ${val}`);
    }
  }
}

